package com.lab.booktwo2.programs.basics;

public class AssignmentOperators {

	public static void main(String[] args) {
		int num =200;
		num -=100;
		System.out.println("After subtract 100 from num = "+num);
		num += 50;
		System.out.println("After adding 50 to the difference = "+num);
		num *= num;
		System.out.println("Square of the sum = "+num);
		num /= 2;
		System.out.println("Half of what the previous result was = "+num);
	}

}
